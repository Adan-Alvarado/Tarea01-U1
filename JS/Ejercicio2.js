/**Resta de dos números: 
Cree un programa que pida dos números y muestre la resta del primero menos el segundo.*/

const numero1 = parseFloat(prompt("Ingrese el primer numero:"));
const numero2 = parseFloat(prompt("Ingrese el segundo numero:"));
const resta = numero1 - numero2;

    
console.log("La resta de los dos numeros es: ", resta);